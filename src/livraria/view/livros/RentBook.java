    public void performInsertTable(ArrayList<Livro> livros) {
        DefaultTableModel model = (DefaultTableModel) tableLivros.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            model.removeRow(i);
        }

        for (Livro livro : livros) {
            model.addRow(new Object[]{
                livro.getId(),
                livro.getCodigo(),
                livro.getNome(),
                livro.getLingua(),
                livro.getAno(),});
        }
    }

